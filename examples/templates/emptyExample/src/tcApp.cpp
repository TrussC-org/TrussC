// ---------------------------------------------------------------------------
// draw - 描画
// ---------------------------------------------------------------------------
void tcApp::draw() {
    // 時間に応じて色を変化させる
    double t = tc::getElapsedTime();
    float r = (float)(sin(t * 0.5) * 0.5 + 0.5);
    float g = (float)(sin(t * 0.7) * 0.5 + 0.5);
    float b = (float)(sin(t * 1.1) * 0.5 + 0.5);

    tc::clear(r, g, b);
}
